/// \file test_codec_dispatch_e2e.cpp
/// \brief E2E tests for multi-codec PCL communication.
///
/// Validates that two components can communicate via the codec dispatch layer
/// with different wire codecs configured per-port.
///
/// Test scenarios:
///   1. Publisher (JSON) → Subscriber (JSON): baseline round-trip
///   2. Content type propagation through pcl_msg_t
///   3. Per-port codec selection (JSON + FlatBuffers on different topics)
///   4. Default vs explicit codec on generated subscribe wrappers
///   5. JSON codec ser/de round-trip (unit-level, no PCL)
///   6. FlatBuffers-style binary codec pub/sub round-trip
///   7. Protobuf-style binary codec pub/sub round-trip
///   8. Three codecs on three ports, single executor
///   9. Codec mismatch detection (wrong deserialiser)
///
/// The codec dispatch layer routes serialisation based on pcl_msg_t.type_name,
/// which is set per-port at configure time.  Component business logic works
/// with typed structs and never touches raw bytes directly.
///
/// Architecture:
///   component logic → codec_dispatch::serialize() → pcl_msg_t → PCL executor
///   → subscriber callback → codec_dispatch::deserialize() → typed struct
///
/// Hand-written binary codecs:
///   Tests 6–8 implement a minimal binary codec for ObjectEvidence to
///   demonstrate how a real FlatBuffers/Protobuf codec would integrate.
///   The binary format is a simple packed struct — in production this would
///   be generated by flatc / protoc.
#include <gtest/gtest.h>


// ===========================================================================
// Test 14: Cross-codec field fidelity
//
// Serialize the same message with all three codecs, deserialize each,
// and verify all three produce identical typed structs.
// ===========================================================================

TEST(CodecDispatchE2E, CrossCodecFieldFidelity) {
    auto ev = makeTestEvidence();

    // Serialize with all three codecs
    std::string json_bytes  = serializeEvidence(ev, "application/json");
    std::string flat_bytes  = serializeEvidence(ev, "application/flatbuffers");
    std::string proto_bytes = serializeEvidence(ev, "application/protobuf");

    // Deserialize each
    codec::ObjectEvidence from_json{}, from_flat{}, from_proto{};
    ASSERT_TRUE(deserializeEvidence(json_bytes.data(), json_bytes.size(),
                                     "application/json", from_json));
    ASSERT_TRUE(deserializeEvidence(flat_bytes.data(), flat_bytes.size(),
                                     "application/flatbuffers", from_flat));
    ASSERT_TRUE(deserializeEvidence(proto_bytes.data(), proto_bytes.size(),
                                     "application/protobuf", from_proto));

    // All three must produce identical results
    expectEvidenceEqual(from_json, ev);
    expectEvidenceEqual(from_flat, ev);
    expectEvidenceEqual(from_proto, ev);
}

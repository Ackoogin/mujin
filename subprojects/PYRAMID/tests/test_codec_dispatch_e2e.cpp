// Test 4: Content type propagation
//
// Verify that the content_type string set at port creation time is correctly
// propagated through pcl_msg_t.type_name to the subscriber callback.
// ===========================================================================

TEST(CodecDispatchE2E, ContentTypePropagation) {
    CodecPubSubState state;
    state.content_type = "application/flatbuffers";

    pcl_executor_t* exec = pcl_executor_create();
    ASSERT_NE(exec, nullptr);

    pcl_callbacks_t pub_cbs{};
    pub_cbs.on_configure = configure_codec_pub;
    pcl_container_t* pub_c =
        pcl_container_create("fb_publisher", &pub_cbs, &state);
    ASSERT_NE(pub_c, nullptr);
    pcl_container_configure(pub_c);
    pcl_container_activate(pub_c);
    pcl_executor_add(exec, pub_c);

    pcl_callbacks_t sub_cbs{};
    sub_cbs.on_configure = configure_codec_sub;
    pcl_container_t* sub_c =
        pcl_container_create("fb_subscriber", &sub_cbs, &state);
    ASSERT_NE(sub_c, nullptr);
    pcl_container_configure(sub_c);
    pcl_container_activate(sub_c);
    pcl_executor_add(exec, sub_c);

    // Publish binary payload
    auto ev = makeTestEvidence();
    std::string payload = flatSerialize(ev);
    pcl_status_t rc = cons::publishObjectEvidence(
        state.pub_port, payload, "application/flatbuffers");
    ASSERT_EQ(rc, PCL_OK);

    for (int i = 0; i < 10; ++i) {
        pcl_executor_spin_once(exec, 0);
        if (state.messages_received.load() > 0) break;
    }

    EXPECT_GE(state.messages_received.load(), 1);
    EXPECT_EQ(state.last_type_name, "application/flatbuffers");
    EXPECT_EQ(sizeof(FlatObjectEvidence), payload.size());

    pcl_executor_destroy(exec);
    pcl_container_destroy(sub_c);
    pcl_container_destroy(pub_c);
}

// ===========================================================================
// Test 5: Per-port codec selection — three codecs on three ports
//
// A single publisher container has three ports, one per codec.
// A single subscriber container subscribes to all three.
// Each subscriber verifies it received the correct content_type and that
// the payload deserialises correctly with the matching codec.
// ===========================================================================

namespace {

struct ThreeCodecState {
    // Publishers (one per codec)
    pcl_port_t* json_pub = nullptr;
    pcl_port_t* flat_pub = nullptr;
    pcl_port_t* proto_pub = nullptr;

    // Subscriber receives
    struct Received {
        std::atomic<int>    count{0};
        std::string         type_name;
        codec::ObjectEvidence evidence{};
        bool                ok = false;
    };
    Received json_rx, flat_rx, proto_rx;
};

void on_json_rx(pcl_container_t*, const pcl_msg_t* msg, void* ud) {
    auto* st = static_cast<ThreeCodecState*>(ud);
    if (!msg || !msg->data) return;
    st->json_rx.type_name = msg->type_name ? msg->type_name : "";
    st->json_rx.ok = deserializeEvidence(
        msg->data, msg->size, "application/json", st->json_rx.evidence);
    st->json_rx.count.fetch_add(1);
}

void on_flat_rx(pcl_container_t*, const pcl_msg_t* msg, void* ud) {
    auto* st = static_cast<ThreeCodecState*>(ud);
    if (!msg || !msg->data) return;
    st->flat_rx.type_name = msg->type_name ? msg->type_name : "";
    st->flat_rx.ok = deserializeEvidence(
        msg->data, msg->size, "application/flatbuffers", st->flat_rx.evidence);
    st->flat_rx.count.fetch_add(1);
}

void on_proto_rx(pcl_container_t*, const pcl_msg_t* msg, void* ud) {
    auto* st = static_cast<ThreeCodecState*>(ud);
    if (!msg || !msg->data) return;
    st->proto_rx.type_name = msg->type_name ? msg->type_name : "";
    st->proto_rx.ok = deserializeEvidence(
        msg->data, msg->size, "application/protobuf", st->proto_rx.evidence);
    st->proto_rx.count.fetch_add(1);
}

pcl_status_t configure_three_pub(pcl_container_t* c, void* ud) {
    auto* st = static_cast<ThreeCodecState*>(ud);
    st->json_pub  = pcl_container_add_publisher(c, "test.json",  "application/json");
    st->flat_pub  = pcl_container_add_publisher(c, "test.flat",  "application/flatbuffers");
    st->proto_pub = pcl_container_add_publisher(c, "test.proto", "application/protobuf");
    return PCL_OK;
}

pcl_status_t configure_three_sub(pcl_container_t* c, void* ud) {
    pcl_container_add_subscriber(c, "test.json",  "application/json",        on_json_rx,  ud);
    pcl_container_add_subscriber(c, "test.flat",  "application/flatbuffers", on_flat_rx,  ud);
    pcl_container_add_subscriber(c, "test.proto", "application/protobuf",    on_proto_rx, ud);
    return PCL_OK;
}

}  // namespace

TEST(CodecDispatchE2E, ThreeCodecsThreePorts) {
    ThreeCodecState state;
    auto ev = makeTestEvidence();

    pcl_executor_t* exec = pcl_executor_create();
    ASSERT_NE(exec, nullptr);

    // Publisher
    pcl_callbacks_t pub_cbs{};
    pub_cbs.on_configure = configure_three_pub;
    pcl_container_t* pub_c =
        pcl_container_create("three_pub", &pub_cbs, &state);
    ASSERT_NE(pub_c, nullptr);
    pcl_container_configure(pub_c);
    pcl_container_activate(pub_c);
    pcl_executor_add(exec, pub_c);

    // Subscriber
    pcl_callbacks_t sub_cbs{};
    sub_cbs.on_configure = configure_three_sub;
    pcl_container_t* sub_c =
        pcl_container_create("three_sub", &sub_cbs, &state);
    ASSERT_NE(sub_c, nullptr);
    pcl_container_configure(sub_c);
    pcl_container_activate(sub_c);
    pcl_executor_add(exec, sub_c);

    // Publish on all three topics, each with its own codec
    {
        std::string p = codec::toJson(ev);
        pcl_msg_t msg{};
        msg.data = p.data();
        msg.size = static_cast<uint32_t>(p.size());
        msg.type_name = "application/json";
        pcl_port_publish(state.json_pub, &msg);
    }
    {
        std::string p = flatSerialize(ev);
        pcl_msg_t msg{};
        msg.data = p.data();
        msg.size = static_cast<uint32_t>(p.size());
        msg.type_name = "application/flatbuffers";
        pcl_port_publish(state.flat_pub, &msg);
    }
    {
        std::string p = protoSerialize(ev);
        pcl_msg_t msg{};
        msg.data = p.data();
        msg.size = static_cast<uint32_t>(p.size());
        msg.type_name = "application/protobuf";
        pcl_port_publish(state.proto_pub, &msg);
    }

    // Spin to deliver all three
    for (int i = 0; i < 20; ++i) {
        pcl_executor_spin_once(exec, 0);
        if (state.json_rx.count.load() > 0 &&
            state.flat_rx.count.load() > 0 &&
            state.proto_rx.count.load() > 0)
            break;
    }

    // Verify JSON
    ASSERT_GE(state.json_rx.count.load(), 1);
    EXPECT_EQ(state.json_rx.type_name, "application/json");
    EXPECT_TRUE(state.json_rx.ok);
    expectEvidenceEqual(state.json_rx.evidence, ev);

    // Verify FlatBuffers
    ASSERT_GE(state.flat_rx.count.load(), 1);
    EXPECT_EQ(state.flat_rx.type_name, "application/flatbuffers");
    EXPECT_TRUE(state.flat_rx.ok);
    expectEvidenceEqual(state.flat_rx.evidence, ev);

    // Verify Protobuf
    ASSERT_GE(state.proto_rx.count.load(), 1);
    EXPECT_EQ(state.proto_rx.type_name, "application/protobuf");
    EXPECT_TRUE(state.proto_rx.ok);
    expectEvidenceEqual(state.proto_rx.evidence, ev);

    pcl_executor_destroy(exec);
    pcl_container_destroy(sub_c);
    pcl_container_destroy(pub_c);
}

// ===========================================================================
// Test 6: Codec mismatch detection
//
// Publish with FlatBuffers codec but attempt to deserialise as Protobuf.
// The magic header should cause deserialisation to fail gracefully.
// ===========================================================================

TEST(CodecDispatchE2E, CodecMismatchDetection) {
    auto ev = makeTestEvidence();

    // Serialize as FlatBuffers
    std::string flat_payload = flatSerialize(ev);

    // Try to deserialize as Protobuf — should fail (wrong magic)
    codec::ObjectEvidence out{};
    bool ok = protoDeserialize(flat_payload.data(), flat_payload.size(), out);
    EXPECT_FALSE(ok) << "protobuf deserializer should reject flatbuffers data";

    // Try to deserialize as FlatBuffers — should succeed
    ok = flatDeserialize(flat_payload.data(), flat_payload.size(), out);
    EXPECT_TRUE(ok);
    expectEvidenceEqual(out, ev);

    // Serialize as Protobuf
    std::string proto_payload = protoSerialize(ev);

    // Try to deserialize as FlatBuffers — should fail
    ok = flatDeserialize(proto_payload.data(), proto_payload.size(), out);
    EXPECT_FALSE(ok) << "flatbuffers deserializer should reject protobuf data";
}

// ===========================================================================
// Test 7: Binary payload size verification
//
// Verify that binary codecs produce smaller payloads than JSON for the
// same message — a key reason for using binary codecs.
// ===========================================================================

TEST(CodecDispatchE2E, BinaryCodecsSmallerThanJson) {
    auto ev = makeTestEvidence();

    std::string json_payload  = codec::toJson(ev);
    std::string flat_payload  = flatSerialize(ev);
    std::string proto_payload = protoSerialize(ev);

    // Binary formats should be smaller than JSON
    EXPECT_LT(flat_payload.size(), json_payload.size())
        << "FlatBuffers payload should be smaller than JSON";
    EXPECT_LT(proto_payload.size(), json_payload.size())
        << "Protobuf payload should be smaller than JSON";

    // Both binary formats have the same size (same struct layout)
    EXPECT_EQ(flat_payload.size(), proto_payload.size());

    // Verify exact binary size: 4 (magic) + 4 + 4 + 8*4 = 44 bytes
    EXPECT_EQ(flat_payload.size(), sizeof(FlatObjectEvidence));
    EXPECT_EQ(proto_payload.size(), sizeof(ProtoObjectEvidence));
}

